<h1>Ansattrettet Bygningsinformasjon</h1>
<p>Fjerdingen er et campus med mange nye muligheter. Det nye bygget vil gi oss som ansatte de beste fasilitetene mulig for å lage interessante og relevante forelesninger. De nye fasilitetene vil også utvide mulighetene for prosjektarbeid vi kan gi studentene. Med gode labber og studioer tilgjengelig, kan vi ta høyde for at elevene har dette utstyret tilgjengelig, og dermed gi de prosjekter som hvor de har bruk for denne typen utstyr. 

Nye forelesnings auditorier med kode tilkoblingsmuligheter, fungerende lerret og gode høytallere vil gjøre det smertefritt å komme i gang med forelesningen. Det blir ikke lenger nødvendig å bruke 15 minutter på starten av forelesning for å få ting til å fungere! 
