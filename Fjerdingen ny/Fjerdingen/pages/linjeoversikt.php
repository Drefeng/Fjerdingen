<b>Mye praksis</b>
<br>
Undervisningen på Westerdals Oslo ACT kjennetegnes av tverrfaglighet, kreativitet, teamarbeid og bransjenærhet. Som student vil du få praktisk erfaring med reelle kommunikasjons- og PR-utfordringer. Du vil få jobbe med hele strategiprosessen fra idé til evaluering av måloppnåelse. Studiet er praktisk rettet, og du vil jobbe tverrfaglig og tilbringe seks måneder ute i bedrift/organisasjon.
God kommunikasjon krever god kjennskap til de du skal kommunisere med. Gjennom internasjonalt samarbeid åpnes studentenes muligheter for å skape god kommunikasjon og løse PR-utfordringer i andre land enn Norge. Det er lagt opp til internasjonale studieturer og mulighet for utveksling ved samarbeidsskoler i utlandet i løpet av studiet.
Etter endt utdanning vil mulige jobber innbefatte rådgivende stillinger og pressehåndtering, samt strategisk PR tilknyttet nasjonale partiorgan, politikere, organisasjoner og internasjonalt samarbeid, og som rådgiver i kommunikasjonsbyrå.
<br>
<br>
<b>Hva er strategi?</b>
<br>
Strategi har i dag blitt en del av folks dagligtale – noe vi tar for gitt hva betyr. Men straks du forsøker deg på en definisjon vil du raskt forville deg inn i ulendt terreng og innse at det ikke finnes én enkel definisjon å stille seg bak. Hva som er strategi, hvordan den skal utvikles og hva den skal inneholde avhenger av det teoretiske landskapet du er i. Dette landskapet vil legge føringer for hvordan strategier utvikles i praksis.
Forenklet kan vi si at ’strategi’ sier noe om målene til en organisasjon eller bedrift på lang sikt. Det målet kan være å å bli best i en bransje (selge flest boller i Norge), vinne valget eller få halvert verdens klimautslipp. Veiene til å nå disse målene vil nødvendigvis være svært forskjellige. I dette bachelorprogrammet vil du lære hva strategi er, hvordan den bør iverksettes og hvilke elementer som er sentrale å tenke gjennom i strategiarbeid.
Når du skal legge opp en strategi er det flere ting som spiller inn. Eksterne elementer, som den samfunnsmessig sammenhengen strategien er en del av, folks medie- og forbruksvaner eller målgruppene som skal nås, er noen slike sentrale byggestener. I tillegg spiller interne faktorer som ledelse, organsiasjonsdesign, teknologi, kommunikasjon og ansatte viktige nøkkelroller.
